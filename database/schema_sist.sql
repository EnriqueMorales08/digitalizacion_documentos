-- =====================================================
-- TABLA PRINCIPAL: SIST_ORDEN_COMPRA
-- Contiene todos los campos de la orden de compra
-- =====================================================

CREATE TABLE SIST_ORDEN_COMPRA (
    OC_ID INT IDENTITY(1,1) PRIMARY KEY,
    OC_FECHA_CREACION DATETIME DEFAULT GETDATE(),

