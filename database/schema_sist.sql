    OC_COPROPIETARIO_DNI NVARCHAR(20),
    OC_REPRESENTANTE_LEGAL NVARCHAR(200),
    OC_REPRESENTANTE_DNI NVARCHAR(20),

    -- DATOS DEL VEHÍCULO (orden del formulario)
