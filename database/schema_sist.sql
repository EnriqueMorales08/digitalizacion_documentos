    OC_HOBBIES_CLIENTE NVARCHAR(200),
    OC_FECHA_NACIMIENTO_NUEVO DATE, -- Segunda fecha de nacimiento (junto a hobbies)

    -- TARJETA DE PROPIEDAD (orden del formulario)
    OC_TARJETA_PROPIEDAD NVARCHAR(50), -- natural, ruc, juridica

